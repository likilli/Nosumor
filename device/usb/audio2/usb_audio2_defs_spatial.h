#ifndef USB_AUDIO2_DEFS_SPATIAL_H
#define USB_AUDIO2_DEFS_SPATIAL_H

#define SP_FL	(1ul << 0)	// Front Left
#define SP_FR	(1ul << 1)	// Front Right
#define SP_FC	(1ul << 2)	// Front Center
#define SP_LFE	(1ul << 3)	// Low Frequency Effects
#define SP_BL	(1ul << 4)	// Back Left
#define SP_BR	(1ul << 5)	// Back Right
#define SP_FLC	(1ul << 6)	// Front Left of Center
#define SP_FRC	(1ul << 7)	// Front Right of Center
#define SP_BC	(1ul << 8)	// Back Center
#define SP_SL	(1ul << 9)	// Side Left
#define SP_SR	(1ul << 10)	// Side Right
#define SP_TC	(1ul << 11)	// Top Center
#define SP_TFL	(1ul << 12)	// Top Front Left
#define SP_TFC	(1ul << 13)	// Top Front Center
#define SP_TFR	(1ul << 14)	// Top Front Right
#define SP_TBL	(1ul << 15)	// Top Back Left
#define SP_TBC	(1ul << 16)	// Top Back Center
#define SP_TBR	(1ul << 17)	// Top Back Right
#define SP_TFLC	(1ul << 18)	// Top Front Left of Center
#define SP_TFRC	(1ul << 19)	// Top Front Right of Center
#define SP_LLFE	(1ul << 20)	// Left Low Frequency Effects
#define SP_RLFE	(1ul << 21)	// Right Low Frequency Effects
#define SP_TSL	(1ul << 22)	// Top Side Left
#define SP_TSR	(1ul << 23)	// Top Side Right
#define SP_BTC	(1ul << 24)	// Bottom Center
#define SP_BLC	(1ul << 25)	// Back Left of Center
#define SP_BRC	(1ul << 26)	// Back Right of Center
#define SP_RD	(1ul << 31)	// Raw Data

#endif // USB_AUDIO2_DEFS_SPATIAL_H
